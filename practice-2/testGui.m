% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global X;global Y;global C;
n=str2double(get(handles.edit5,'String'));
n=n+1;
X(n)=str2double(get(handles.edit1,'String'));
Y(n)=str2double(get(handles.edit2,'String'));
f=1;
for i=1:n-1
    for j=i+1:n
        if X(i)==X(j)
            f=0;
            n=n-1;
            errordlg('The data X must be unequal from each other!','错误');
            break;
        end
    end
    if f==0
        break;
    end
end
if n==1
    C(1,1)=Y(1);
else
    C(n,1)=Y(n);
    for i=2:n
        C(n,i)=(C(n,i-1)-C(n-1,i-1))/(X(n)-X(n-i+1));
    end
end
set(handles.edit5,'String',n);


