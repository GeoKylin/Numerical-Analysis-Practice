clear all
close all
clc

N=20000000;       %迭代次数
an=1:N+1;       %系数序列
x=0.1;         %未知数x

P1=0;           %算法5.5的初始值
b=1;            %算法5.5式中的x^k项
for i=0:N
   P1= P1+an(1,i+1)*b;  %迭代计算P
   b=b*x;               %每次迭代乘以x，以减少幂函数的使用
end
P1              %输出P

S(1,N+1)=an(1,N+1);     %算法5.6的初始值
for i=N-1:-1:0
   S(1,i+1)=x*S(1,i+2)+an(1,i+1);   %迭代计算P
end
P2=S(1,1)       %输出P